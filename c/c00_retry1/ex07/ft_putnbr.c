#include <unistd.h>

void	ft_putnbr(int nb)
{
	unsigned int	n;

	if (nb < 0)
	{
		write(1, "-", 1);
		n = -nb;
	}
	else
	{
		n = nb;
	}
	if (n / 10 > 0)
	{
		ft_putnbr(n / 10);
	}
	write(1, &("0123456789"[n % 10]), 1);
}
